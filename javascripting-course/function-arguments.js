/* Defined a function named math that takes 3 arguments. */
function math (firstArg, secondArg, thirdArg) {
    /* Return the value obtained from multiplying the second
     and third arguments and adding that result to the first argument. */ 
    return secondArg * thirdArg + firstArg 
}
/* Call math() function with the number 53 as first argument, the number 61
 as second and the number 67 as the third argument. */
console.log(math(53, 61, 67));